# arithmetic operator -> +, -, *, /, %, **
a = 5 
b = 2

sum = a + b 
sub = a - b
multi = a * b
div = a / b
rem = a % b                                 #remainder
pow = a ** b                                #a^b

print(sum)                                  #output: 7
print(sub)                                  #output: 3
print(multi)                                #output: 10
print(div)                                  #output: 2.5
print(rem)                                  #output: 1
print(pow)                                  #output: 25

# relational operator -> ==, !=, >, >=, <, <=
c = 50
d = 20

print(c == d)                               #output: False
print(c != d)                               #output: True
print(c >= d)                               #output: True
print(c <= d)                               #output: False

# assignment operator -> =, +=, -=, *=, /=, %=, **=
num = 10
num += 5                                    #num = num + 5 => 15
print(num)  
num -= 5                                    #num = num - 5 => 10
print(num)
num *= 3                                    #num = num * 3 => 30
print(num)
num /= 3                                    #num = num / 3 => 10.0
print(num)
num %= 4                                    #num = num % 3 => 2.0
print(num)
num **= 3                                   #num = num ** 3 => 8.0
print(num)

