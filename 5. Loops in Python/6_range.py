for i in range(5):
    print(i)                          #returns 0 to 4

for i in range(1, 5):
    print(i)                          #returns 1 to 4

for i in range (1, 5, 2):
    print(i)                          #returns 1 to 3

for i in range (5, 0, -1):
    print(i)                          #returns 5 to 1